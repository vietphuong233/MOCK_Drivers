/******************************************************************************
 * Code
 ******************************************************************************/
void NVIC_EnableIRQ(IRQn_Type IRQn)
{
    if ((int32_t)(IRQn) >= 0)
    {
        S32_NVIC->ISER[(uint32_t)(IRQn) >> 5U] |=
                       (uint32_t)(1UL << ((uint32_t)(IRQn) & (uint32_t)0x1FU));
    }
}

