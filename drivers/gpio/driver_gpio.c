#include "stdlib.h"
#include "driver_gpio.h"

/******************************************************************************
 * Code
 ******************************************************************************/
void GPIO_DRV_PinInit(GPIO_Type *base,uint32_t pin,
                      const gpio_pin_config_t *config)
{
    if (config != NULL)
    {
        if (config->pinDirection == GPIO_DigitalOutput)
        {
            base->PDDR |= (1 << pin);
            if (config->outputLogic == 0)
            {
                base->PCOR |= (1 << pin);
            }
            else
            {
                base->PSOR |= (1 << pin);
            }
        }
        else
        {
            base->PDDR &= ~(1 << pin);
        }
    }
}

